class Episode{
  final int id;
  final String name;
  final int episode;
  final int season;
  final String description;
  final String airDate;
  final String image;
  const Episode({
    required this.id,
    required this.name,
    required this.episode,
    required this.season,
    required this.description,
    required this.airDate,
    required this.image
  });
}